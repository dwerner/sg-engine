
#[allow(dead_code)]
#[derive(Copy, Clone)]
pub struct DeltaVector {
    delta_x: i32,
    delta_y: i32,
}

#[allow(dead_code)]
#[derive(Copy, Clone)]
pub struct ScreenRect {
    x: u32,
    y: u32,
    w: u32,
    h: u32,
}

#[allow(dead_code)]
#[derive(Copy, Clone)]
pub struct ScreenPoint {
    x: u32,
    y: u32,
}

