
#[allow(dead_code)]
#[derive(Copy, Clone)]
pub struct DeltaVector {
    pub delta_x: i32,
    pub delta_y: i32,
}

#[allow(dead_code)]
#[derive(Copy, Clone)]
pub struct ScreenRect {
    pub x: i32,
    pub y: i32,
    pub w: i32,
    pub h: i32,
}

#[allow(dead_code)]
#[derive(Copy, Clone)]
pub struct ScreenPoint {
    pub x: i32,
    pub y: i32,
}

